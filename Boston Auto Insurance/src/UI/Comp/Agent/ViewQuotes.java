    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblCustQuoteList.getModel();
        model.setRowCount(0);

        for (CustomerQuote CQ : customerQuoteDirectory.getCustomerQuotes()) {
            Object[] row = new Object[3];
            row[0] = CQ;
            row[1] = CQ.getQuote().getStatus();
            row[2] = CQ.getQuote().getReason();

            model.addRow(row);
        }
    }
