    private void btnContinueQuoteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinueQuoteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCustQuoteList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Quote");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblCustQuoteList.getModel();
        CustomerQuote custQuote = (CustomerQuote) model.getValueAt(selectedRow, 0);

        // Check if the status is unauthorized
        if (custQuote.getQuote().getStatus().equalsIgnoreCase("Unauthorized")) {
            JOptionPane.showMessageDialog(this, "This quote is unauthorized. It must be approved by an underwriter before you can continue.");
            return;
        }

        // Continue to the next page
        CardLayout layout = (CardLayout) workPanel.getLayout();
        AboutYouPage aboutYouPage = new AboutYouPage(BIM, workPanel, custQuote, account);
        workPanel.add("AboutYouPage", aboutYouPage);
        layout.next(workPanel);
    }//GEN-LAST:event_btnContinueQuoteActionPerformed

