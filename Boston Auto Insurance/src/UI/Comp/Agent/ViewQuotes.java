    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCustQuoteList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Quote");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblCustQuoteList.getModel();
        CustomerQuote custQuote = (CustomerQuote) model.getValueAt(selectedRow, 0);

        int customerId = custQuote.getCustomerAccount().getCustomerId();
        txtCustName.setText(custQuote.getCustomerAccount().getFullName());
        txtCustID.setText(String.valueOf(customerId));
        txtEmail.setText(custQuote.getCustomerAccount().getEmail());
    }//GEN-LAST:event_btnViewActionPerformed

