    private void btnRenewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRenewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCustPolicyList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Policy");
            return;
        }

        int response = JOptionPane.showConfirmDialog(this, "Are you sure you want to renew this policy?", "Renew Policy Confirmation", JOptionPane.YES_NO_OPTION);

        if (response == JOptionPane.YES_OPTION) {
            // User confirmed, proceed with renewing the policy
            DefaultTableModel model = (DefaultTableModel) tblCustPolicyList.getModel();
            Policy policy = (Policy) model.getValueAt(selectedRow, 0);

            policy.setStatus("renewed");
            populateTable();
        }
    }//GEN-LAST:event_btnRenewActionPerformed


