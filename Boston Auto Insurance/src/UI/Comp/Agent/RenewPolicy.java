    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblCustPolicyList.getModel();
        model.setRowCount(0);

        for (CustomerPolicy CP : customerPolicyDirectory.getCustomerPolicies()) {
            if (!"renewed".equals(CP.getPolicy().getStatus())) {
                Object[] row = new Object[2];
                row[0] = CP.getPolicy();
                row[1] = CP.getPolicy().getStatus();

                model.addRow(row);
            }
        }
    }

