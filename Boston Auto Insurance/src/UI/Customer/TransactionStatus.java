    private void populatePolicyList() {
        DefaultTableModel model = (DefaultTableModel) tblCustPolicyList.getModel();
        model.setRowCount(0);

        CustomerPolicy customerPolicy = customerPolicyDirectory.findCustomerPolicy(cust);

        if (customerPolicy != null) {
            Object[] row = {customerPolicy, customerPolicy.getPolicy().getStatus()};
            model.addRow(row);
        }
    }

    private void populateQuoteList() {
        DefaultTableModel model = (DefaultTableModel) tblCustQuoteList1.getModel();
        model.setRowCount(0); // Clear existing rows

        // Get the customer's quote from the customerQuoteDirectory
        CustomerQuote customerQuote = customerQuoteDirectory.findCustomerQuote(cust);

        if (customerQuote != null) {
            // Populate the table with the quote's details
            Object[] row = {customerQuote, customerQuote.getQuote().getStatus()};
            model.addRow(row);
        }
    }

