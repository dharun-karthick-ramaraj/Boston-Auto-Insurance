    private void btnContinueQuoteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinueQuoteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCustQuoteList1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Quote");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblCustQuoteList1.getModel();
        CustomerQuote custQuote = (CustomerQuote) model.getValueAt(selectedRow, 0);

        if (custQuote.getQuote().getStatus().equalsIgnoreCase("Unauthorized")) {
            JOptionPane.showMessageDialog(this, "This quote is unauthorized. It must be approved by an underwriter before you can continue.");
            return;
        }

        CardLayout layout = (CardLayout) workPanel.getLayout();
        UI.Customer.AboutYouPage aboutYouPage = new UI.Customer.AboutYouPage(BIM, workPanel, account, custQuote);
        workPanel.add("AboutYouPage", aboutYouPage);
        layout.next(workPanel);
    }//GEN-LAST:event_btnContinueQuoteActionPerformed

    private void btnworksheetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnworksheetActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCustPolicyList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Select a policy from the above table to view details");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblCustPolicyList.getModel();
        CustomerPolicy custPolicy = (CustomerPolicy) model.getValueAt(selectedRow, 0);
        PolicyWorksheet policyWorksheetPage = new PolicyWorksheet(BIM, workPanel, custPolicy);
        workPanel.add(policyWorksheetPage, "PolicyWorksheetPage");
        CardLayout layout = (CardLayout) workPanel.getLayout();
        layout.show(workPanel, "PolicyWorksheetPage");
    }//GEN-LAST:event_btnworksheetActionPerformed


