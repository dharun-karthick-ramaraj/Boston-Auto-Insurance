    private void populatePage() {
        String firstName = custPolicy.getCustomerAccount().getFirstName();
        String lastName = custPolicy.getCustomerAccount().getLastName();
        String streetAddress = custPolicy.getCustomerAccount().getStreetAddress();
        String city = custPolicy.getCustomerAccount().getCity();
        String state = custPolicy.getCustomerAccount().getState();
        String zipCode = custPolicy.getCustomerAccount().getZip();
        String dob = custPolicy.getCustomerAccount().getDob();
        String email = custPolicy.getCustomerAccount().getEmail();
        String phone = custPolicy.getCustomerAccount().getPhone();
        String userName = custPolicy.getCustomerAccount().getAccountCredentials().getUserID();

        txtFName.setText(firstName);
        txtLName.setText(lastName);
        txtStreetAdd.setText(streetAddress);
        txtCity.setText(city);
        txtState.setText(state);
        txtZip.setText(zipCode);
        txtDob.setText(dob);
        txtEmail.setText(email);
        txtPhone.setText(phone);
        txtUserName.setText(userName);

        DefaultTableModel model = (DefaultTableModel) tblAddedVehList.getModel();
        model.setRowCount(0);
        for (Vehicle vehicle : custPolicy.getPolicy().getVehicles()) {
            Object[] row = {
                vehicle.getVin(),
                vehicle.getYear(),
                vehicle.getMake(),
                vehicle.getModel(),
                vehicle.getOwnership(),
                vehicle.getUse(),
                vehicle.getRegisteration()
            };
            model.addRow(row);
        }

        txtVehiclecount.setText(String.valueOf(custPolicy.getPolicy().getVehicles().size()));
        txtPolicyNumber.setText(custPolicy.getPolicy().getPolicyId());
        txtPolicyStatus.setText(custPolicy.getPolicy().getStatus());
        txtCustomerID.setText(String.valueOf(custPolicy.getCustomerAccount().getCustomerId()));
        txtPackageSel.setText(custPolicy.getPolicy().getPackageType());
        //txtDisApplied.setText(String.valueOf(custPolicy.getPolicy()));
        txtTotalAmt.setText(String.valueOf(custPolicy.getPolicy().getPremiumAmount()));
    }
